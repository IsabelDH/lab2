        <h2>Europees niveau</h2>
        <p>De EU-begroting, ook bekend als het meerjarig financieel kader (MFK), is een langetermijnuitgavenplan voor de Europese Unie dat voor een periode van vijf tot zeven jaar wordt vastgesteld. Het MFK definieert de prioriteiten en grenzen voor de EU-uitgaven. Het grootste deel van de begroting gaat naar investeringen, variërend van plattelandsontwikkeling en milieubehoud tot de bescherming van de buitengrenzen en mensenrechten.
            Het beheer van het EU-geld wordt gedeeld tussen nationale autoriteiten en de Europese Commissie (75%), de Europese Commissie en haar agentschappen (18%), en andere internationale organisaties, nationale agentschappen of niet-EU-landen (8%). Binnen de huidige begroting (2021-2027) wordt 90% van de middelen voor NextGenerationEU via een direct beheerde faciliteit voor herstel en veerkracht verstrekt.
            De begroting wordt opgesteld door de Europese Commissie, de Raad en het Europees Parlement gezamenlijk. De Commissie stelt een ontwerpbegroting voor, die wordt aangepast door de Raad en het Parlement. Uitgaven van de EU worden verdeeld over zeven categorieën, met prioriteiten zoals economische, sociale en territoriale cohesie, het aanpakken van de gevolgen van internationale crises zoals de oorlog in Oekraïne, duurzaam herstel na COVID-19, digitale transitie, en veiligheid.
            Dit uitgavenplan stelt de EU in staat haar financieringsprogramma's enkele jaren vooruit te plannen en gerichte investeringen te doen die de Europese economie en samenleving versterken.
        </p>
