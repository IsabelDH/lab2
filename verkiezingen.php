            <h2>Europees niveau</h2>
            <p>Het Europese Parlement heeft een cruciale rol in de vaststelling van EU-wetgeving en de EU-begroting, die de komende jaren de basis vormt voor belangrijke investeringen in verschillende sectoren zoals duurzaamheid, digitale transformatie en sociaal-economische cohesie. Bij de Europese verkiezingen kunnen we onze vertegenwoordigers kiezen die zullen meebeslissen over deze zaken, en die onze belangen op het Europese toneel zullen verdedigen.</p> <br>
            <p> Daarnaast behandelt het Europees Parlement belangrijke kwesties zoals de klimaatverandering, grensoverschrijdende criminaliteit en de versterking van de interne markt. Door te stemmen voor het Europees Parlement kunnen we bijdragen aan beslissingen die een directe impact hebben op onze veiligheid, economie en levenskwaliteit. Het Parlement speelt ook een essentiële rol in de bescherming van democratische waarden en mensenrechten binnen de EU.</p>

        </div>

        <div>
            <h2>Federaal niveau</h2>
            <p>De federale verkiezingen bepalen de samenstelling van de Kamer van Volksvertegenwoordigers, die vervolgens de federale regering vormt. Deze regering is verantwoordelijk voor belangrijke beleidsdomeinen zoals justitie, defensie, sociale zekerheid en nationale belastingen. Onze stem helpt bepalen wie beslissingen neemt over zaken die ons dagelijks leven rechtstreeks beïnvloeden, zoals gezondheidszorg, werkgelegenheid en pensioenen.</p> <br>
            <p>Daarnaast is de federale overheid verantwoordelijk voor het beheren van de nationale infrastructuur, het waarborgen van de binnenlandse veiligheid en het onderhouden van buitenlandse betrekkingen. Het federale beleid bepaalt ook de verdeling van financiële middelen tussen de verschillende regio's, wat cruciaal is voor het functioneren van België als federale staat.</p>
            </p>
        </div>
        <div>
            <h2>Regionaal niveau</h2>
            <p>
                De regionale verkiezingen in Vlaanderen bepalen de samenstelling van het Vlaams Parlement, dat bevoegd is voor een breed scala aan zaken, waaronder onderwijs, cultuur, milieu en mobiliteit. Het is belangrijk om te stemmen voor vertegenwoordigers die onze lokale en regionale belangen kunnen behartigen en beleid kunnen vormgeven dat aansluit bij de specifieke noden en prioriteiten van Vlaanderen.</p><br>
            <p>Het regionale beleid heeft directe invloed op ons dagelijks leven, van de kwaliteit van ons onderwijs en de efficiëntie van onze gezondheidszorg tot de bereikbaarheid van openbaar vervoer en de bescherming van ons milieu. De Vlaamse regering speelt ook een sleutelrol in het stimuleren van de economische ontwikkeling en het creëren van werkgelegenheid binnen de regio. </p>
        </div>

